let diaDaSema = 4

switch (diaDaSema) {
    case 1:
        console.log('segunda-feira, boa semana!');
        
        break;
        case 2:
            console.log('terça-feira, bora pra cima!!');
            
            break;
            case 3:
                console.log('quarta-feira, otima semana!');
                
                break;
                case 4:
                    console.log('quinta-feira, vamo pra cima!');
                    
                    break;
                    case 5:
                        console.log('sesta-feira, tá chegando o feriado!');
                        
                        break;
                        case 6:
                            console.log('sabado, bom descanço!');
                            
                            break;
                            case 7:
                                console.log('domingo, bom domingo!');
                                
                                break;
                                                                                    
    default:
        break;
}